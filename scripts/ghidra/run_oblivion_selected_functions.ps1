  throw "Ghidra analyzeHeadless not found: $analyzeHeadless"
}

if (-not (Test-Path $BinaryPath)) {
  throw "Required binary not found: $BinaryPath"
}

$repoRoot = (Resolve-Path (Join-Path $PSScriptRoot "..\..")).Path
if ([System.IO.Path]::IsPathRooted($OutputRoot)) {
  $outputAbs = $OutputRoot
} else {
  $outputAbs = Join-Path $repoRoot $OutputRoot
}

New-Item -ItemType Directory -Force -Path $outputAbs | Out-Null
$outputAbs = (Resolve-Path $outputAbs).Path

$projectRoot = Join-Path $outputAbs "tmp-ghidra-project"
$scriptPath = Join-Path $repoRoot "scripts\ghidra"
$targetList = $Targets | Where-Object { $_ -and $_.Trim().Length -gt 0 }

if (-not $targetList) {
  throw "At least one target function must be specified."
}

New-Item -ItemType Directory -Force -Path $projectRoot | Out-Null
$targetFile = Join-Path $projectRoot "selected-targets.txt"
Set-Content -Path $targetFile -Value $targetList -Encoding UTF8

$args = @(
  $projectRoot,
  "OblivionSelectedFunctions",
  "-import", $BinaryPath,
  "-overwrite",
  "-scriptPath", $scriptPath,
  "-postScript", "ExportSelectedFunctions.java", $outputAbs, "@$targetFile",
  "-deleteProject"
)

& $analyzeHeadless @args
if ($LASTEXITCODE -ne 0) {
  throw "Ghidra selected-function export failed with exit code $LASTEXITCODE."
}

if (Test-Path $projectRoot) {
  Remove-Item -Recurse -Force $projectRoot
}
